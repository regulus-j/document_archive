    <script>
        function workflowDashboard() {
            return {
                activeWorkflows: [],
                pendingTasks: [],
                stats: {},
                loading: true,
                
                async init() {
                    await this.loadDashboardData();
                    // Refresh every 30 seconds
                    setInterval(() => this.loadDashboardData(), 30000);
                },
                
                async loadDashboardData() {
                    try {
                        const response = await fetch('/api/workflows/dashboard', {
                            headers: {
                                'Authorization': 'Bearer ' + document.querySelector('meta[name="api-token"]')?.getAttribute('content'),
                                'Accept': 'application/json',
                                'X-CSRF-TOKEN': document.querySelector('meta[name="csrf-token"]').getAttribute('content')
                            }
                        });
                        
                        if (response.ok) {
                            const data = await response.json();
                            if (data.success) {
                                this.activeWorkflows = data.data.sent_workflows || [];
                                this.pendingTasks = data.data.pending_workflows || [];
                                this.stats = {
                                    active_count: data.data.sent_active_count || 0,
                                    completed_count: data.data.recent_completed_count || 0,
                                    overdue_count: data.data.overdue_count || 0
                                };
                            }
                        }
                    } catch (error) {
                        console.error('Error loading dashboard data:', error);
                    } finally {
                        this.loading = false;
                    }
                },
                
                viewWorkflow(chainId) {
                    window.location.href = `/workflows/chain/${chainId}`;
                },
                
                viewWorkflowStep(workflowId) {
                    window.location.href = `/workflows/step/${workflowId}`;
                },
                
                async pauseWorkflow(chainId) {
                    if (confirm('Are you sure you want to pause/resume this workflow?')) {
                        try {
                            const response = await fetch(`/api/workflows/chain/${chainId}/toggle-pause`, {
                                method: 'PATCH',
                                headers: {
                                    'Authorization': 'Bearer ' + document.querySelector('meta[name="api-token"]')?.getAttribute('content'),
                                    'Accept': 'application/json',
                                    'Content-Type': 'application/json',
                                    'X-CSRF-TOKEN': document.querySelector('meta[name="csrf-token"]').getAttribute('content')
                                }
                            });
                            
                            if (response.ok) {
                                const data = await response.json();
                                if (data.success) {
                                    await this.loadDashboardData();
                                    showNotification('success', `Workflow ${data.data.status} successfully`);
                                }
                            }
                        } catch (error) {
                            console.error('Error toggling workflow:', error);
                            showNotification('error', 'Failed to update workflow status');
                        }
                    }
                },
                
                async cancelWorkflow(chainId) {
                    const reason = prompt('Please provide a reason for canceling this workflow:');
                    if (reason && reason.trim()) {
                        try {
                            const response = await fetch(`/api/workflows/chain/${chainId}/cancel`, {
                                method: 'PATCH',
                                headers: {
                                    'Authorization': 'Bearer ' + document.querySelector('meta[name="api-token"]')?.getAttribute('content'),
                                    'Accept': 'application/json',
                                    'Content-Type': 'application/json',
                                    'X-CSRF-TOKEN': document.querySelector('meta[name="csrf-token"]').getAttribute('content')
                                },
                                body: JSON.stringify({ reason: reason.trim() })
                            });
                            
                            if (response.ok) {
                                const data = await response.json();
                                if (data.success) {
                                    await this.loadDashboardData();
                                    showNotification('success', 'Workflow cancelled successfully');
                                }
                            }
                        } catch (error) {
                            console.error('Error cancelling workflow:', error);
                            showNotification('error', 'Failed to cancel workflow');
                        }
                    }
                },
                
                getStatusBadgeClass(status) {
                    const classes = {
                        'active': 'bg-blue-100 text-blue-800',
                        'completed': 'bg-green-100 text-green-800',
                        'paused': 'bg-yellow-100 text-yellow-800',
                        'cancelled': 'bg-red-100 text-red-800'
                    };
                    return classes[status] || 'bg-gray-100 text-gray-800';
                },
                
                getUrgencyBadgeClass(urgency) {
                    const classes = {
                        'critical': 'bg-red-100 text-red-800',
                        'high': 'bg-orange-100 text-orange-800',
                        'medium': 'bg-yellow-100 text-yellow-800',
                        'low': 'bg-green-100 text-green-800'
                    };
                    return classes[urgency] || 'bg-gray-100 text-gray-800';
                },
                
                formatDate(dateString) {
                    if (!dateString) return '';
                    return new Date(dateString).toLocaleDateString();
                }
            }
        }

        // Global functions
        function showCreateWorkflowModal() {
            document.getElementById('createWorkflowModal').style.display = 'flex';
        }

        function showNotification(type, message) {
            // Create toast notification
            const toast = document.createElement('div');
